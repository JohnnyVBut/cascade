// LoadInterface reads one interface from SQLite and populates its peer cache.
func LoadInterface(id string) (*TunnelInterface, error) {
	t, err := scanInterface(id)
	if err != nil {
		return nil, err
	}
	if err := t.LoadPeers(); err != nil {
		return nil, fmt.Errorf("load peers for %s: %w", id, err)
	}
	return t, nil
}

// ListInterfaceIDs returns all interface IDs ordered by created_at.
// Used by InterfaceManager at startup.
func ListInterfaceIDs() ([]string, error) {
	rows, err := db.DB().Query(`SELECT id FROM interfaces ORDER BY created_at`)
	if err != nil {
		return nil, err
	}
	defer rows.Close()

	var ids []string
	for rows.Next() {
		var id string
		if err := rows.Scan(&id); err != nil {
			return nil, err
		}
		ids = append(ids, id)
	}
	return ids, rows.Err()
}

// Create inserts a new interface into SQLite and returns it.
// Does NOT start the interface; call Start() explicitly.
func Create(inp InterfaceInput) (*TunnelInterface, error) {
	t := &TunnelInterface{
		ID:            inp.ID,
		Name:          inp.Name,
		Address:       inp.Address,
		ListenPort:    inp.ListenPort,
		Protocol:      inp.Protocol,
		Enabled:       false,
		DisableRoutes: inp.DisableRoutes,
		PrivateKey:    inp.PrivateKey,
		PublicKey:     inp.PublicKey,
		AWG2:          inp.AWG2,
		CreatedAt:     time.Now().UTC().Format(time.RFC3339),
		peers:         make(map[string]*peer.Peer),
	}
	if err := t.save(); err != nil {
		return nil, err
	}
	return t, nil
}

// Update applies non-nil fields from upd, persists, regenerates config, and hot-reloads.
func (t *TunnelInterface) Update(upd InterfaceUpdate) error {
	if upd.Name != nil {
		t.Name = strings.TrimSpace(*upd.Name)
	}
	if upd.Address != nil {
		t.Address = strings.TrimSpace(*upd.Address)
	}
	if upd.ListenPort != nil {
		t.ListenPort = *upd.ListenPort
	}
	if upd.DisableRoutes != nil {
		t.DisableRoutes = *upd.DisableRoutes
	}
	if upd.AWG2 != nil {
		t.AWG2 = upd.AWG2
	}
	if err := t.save(); err != nil {
		return err
	}
	if err := t.RegenerateConfig(); err != nil {
		return err
	}
	if t.Enabled {
		t.Reload() // hot-reload via syncconf (fire-and-forget)
	}
	return nil
}

// Delete stops the interface, removes all its peers from SQLite, deletes the
// interface row, and removes the wg-quick config file from disk.
func (t *TunnelInterface) Delete() error {
	if t.Enabled {
		if err := t.Stop(); err != nil {
			log.Printf("tunnel: delete %s: stop failed (continuing): %v", t.ID, err)
		}
	}

	// Remove all peers from SQLite.
	t.peersMu.RLock()
	peerIDs := make([]string, 0, len(t.peers))
	for id := range t.peers {
		peerIDs = append(peerIDs, id)
	}
	t.peersMu.RUnlock()

	for _, pid := range peerIDs {
		if err := peer.DeletePeer(pid); err != nil {
			log.Printf("tunnel: delete %s: remove peer %s: %v", t.ID, pid, err)
		}
	}

	if _, err := db.DB().Exec(`DELETE FROM interfaces WHERE id = ?`, t.ID); err != nil {
		return fmt.Errorf("delete interface %s: %w", t.ID, err)
	}
	// Config file is best-effort (may not exist if interface was never started).
	_ = os.Remove(fmt.Sprintf("/etc/amnezia/amneziawg/%s.conf", t.ID))

	log.Printf("tunnel: interface %s deleted", t.ID)
	return nil
}

// save upserts the TunnelInterface row in SQLite.
// Called internally after every state change (Start, Stop, Update).
func (t *TunnelInterface) save() error {
	// Flatten AWG2 into nullable columns (nil → SQL NULL).
	var jc, jmin, jmax, s1, s2, s3, s4 interface{}
	var h1, h2, h3, h4 interface{}
	var i1, i2, i3, i4, i5 interface{}
	if t.AWG2 != nil {
		a := t.AWG2
		jc, jmin, jmax = a.Jc, a.Jmin, a.Jmax
		s1, s2, s3, s4 = a.S1, a.S2, a.S3, a.S4
		h1, h2, h3, h4 = a.H1, a.H2, a.H3, a.H4
		i1, i2, i3, i4, i5 = a.I1, a.I2, a.I3, a.I4, a.I5
	}

	_, err := db.DB().Exec(`
		INSERT INTO interfaces
			(id, name, address, listen_port, protocol, enabled, disable_routes,
			 private_key, public_key,
			 jc, jmin, jmax, s1, s2, s3, s4, h1, h2, h3, h4,
			 i1, i2, i3, i4, i5, created_at)
		VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)
		ON CONFLICT(id) DO UPDATE SET
			name=excluded.name, address=excluded.address,
			listen_port=excluded.listen_port, protocol=excluded.protocol,
			enabled=excluded.enabled, disable_routes=excluded.disable_routes,
			private_key=excluded.private_key, public_key=excluded.public_key,
			jc=excluded.jc, jmin=excluded.jmin, jmax=excluded.jmax,
			s1=excluded.s1, s2=excluded.s2, s3=excluded.s3, s4=excluded.s4,
			h1=excluded.h1, h2=excluded.h2, h3=excluded.h3, h4=excluded.h4,
			i1=excluded.i1, i2=excluded.i2, i3=excluded.i3, i4=excluded.i4, i5=excluded.i5`,
		t.ID, t.Name, t.Address, t.ListenPort, t.Protocol,
		boolInt(t.Enabled), boolInt(t.DisableRoutes),
		t.PrivateKey, t.PublicKey,
		jc, jmin, jmax, s1, s2, s3, s4,
		h1, h2, h3, h4,
		i1, i2, i3, i4, i5,
		t.CreatedAt,
	)
	return err
}

// ── Peer cache ────────────────────────────────────────────────────────────────

// LoadPeers fetches all peers for this interface from SQLite into the in-memory map.
// Also initialises trafficState from persisted total_rx/total_tx (migration v11)
// so that lifetime totals survive container restarts.
// Called at startup; kept in sync by AddPeer / UpdatePeer / RemovePeer.
func (t *TunnelInterface) LoadPeers() error {
	ps, err := peer.GetPeers(t.ID)
	if err != nil {
		return err
	}
	t.peersMu.Lock()
	t.peers = make(map[string]*peer.Peer, len(ps))
	for i := range ps {
		t.peers[ps[i].ID] = &ps[i]
	}
	t.peersMu.Unlock()

	// Initialise trafficState from DB totals.
	// lastSeenRx/Tx starts at 0: after container restart the kernel counter
	// resets to 0 too, so the first delta is always 0 (no phantom traffic spike).
	t.trafficMu.Lock()
	t.trafficState = make(map[string]*peerTrafficState, len(ps))
	for i := range ps {
		t.trafficState[ps[i].ID] = &peerTrafficState{
			totalRx: ps[i].TotalRx,
			totalTx: ps[i].TotalTx,
		}
	}
	t.trafficMu.Unlock()
	return nil
}

// GetPeer returns the in-memory peer by ID, or nil if not found.
func (t *TunnelInterface) GetPeer(id string) *peer.Peer {
	t.peersMu.RLock()
	defer t.peersMu.RUnlock()
	return t.peers[id]
}

// GetAllPeers returns a snapshot of all in-memory peers.
func (t *TunnelInterface) GetAllPeers() []*peer.Peer {
	t.peersMu.RLock()
	defer t.peersMu.RUnlock()
	out := make([]*peer.Peer, 0, len(t.peers))
	for _, p := range t.peers {
		out = append(out, p)
	}
	// Go map iteration is non-deterministic — sort by CreatedAt so the frontend
	// receives peers in a stable order on every poll tick.
	sort.Slice(out, func(i, j int) bool {
		return out[i].CreatedAt < out[j].CreatedAt
	})
	return out
}

// PeerCount returns the number of peers in the cache.
func (t *TunnelInterface) PeerCount() int {
	t.peersMu.RLock()
	defer t.peersMu.RUnlock()
	return len(t.peers)
}

