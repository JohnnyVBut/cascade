// peerTrafficState tracks per-peer traffic accumulation in memory.
// lastSeenRx/Tx: kernel counter value at the previous poll tick (for delta).
// totalRx/Tx:   lifetime accumulated bytes (loaded from DB on startup, flushed periodically).
// dirty:        true when totalRx/Tx changed since the last DB flush.
type peerTrafficState struct {
	lastSeenRx int64
	lastSeenTx int64
	totalRx    int64
	totalTx    int64
	dirty      bool
}

// TunnelInterface represents a single WireGuard or AmneziaWG tunnel interface.
type TunnelInterface struct {
	reloadMu  sync.Mutex   // serializes Reload/Restart — concurrent syncconf = kernel deadlock (FIX-8, FIX-9)
	peersMu   sync.RWMutex // protects peers map: written by AddPeer/Remove, read by GetStatus goroutine
	trafficMu sync.Mutex   // protects trafficState map

	// ── Persisted (SQLite interfaces table) ──────────────────────────────────
	ID            string             `json:"id"`
	Name          string             `json:"name"`
	Address       string             `json:"address"`        // CIDR e.g. "10.8.0.1/24"
	ListenPort    int                `json:"listenPort"`
	Protocol      string             `json:"protocol"`       // "wireguard-1.0" | "amneziawg-2.0"
	Enabled       bool               `json:"enabled"`
	DisableRoutes bool               `json:"disableRoutes"`  // Table = off (S2S / PBR setups)
	PrivateKey    string             `json:"-"`              // never exposed in API responses
	PublicKey     string             `json:"publicKey"`
	AWG2          *peer.AWG2Settings `json:"settings"`       // nil for wireguard-1.0
	CreatedAt     string             `json:"createdAt"`

	// ── Runtime (in-memory, not persisted) ───────────────────────────────────
	peers        map[string]*peer.Peer        // keyed by peer.ID; protected by peersMu
	trafficState map[string]*peerTrafficState // keyed by peer.ID; protected by trafficMu
}

// InterfaceInput is the payload for creating a new interface.
type InterfaceInput struct {
	ID            string
	Name          string
	Address       string             // CIDR e.g. "10.8.0.1/24"
	ListenPort    int
	Protocol      string             // "wireguard-1.0" | "amneziawg-2.0"
	DisableRoutes bool
	PrivateKey    string             // must be pre-generated by caller
	PublicKey     string             // derived from PrivateKey
	AWG2          *peer.AWG2Settings // nil for wireguard-1.0
}

// InterfaceUpdate holds the mutable fields changeable via PATCH.
// nil pointer = do not change that field.
type InterfaceUpdate struct {
	Name          *string
	Address       *string
	ListenPort    *int
	DisableRoutes *bool
	AWG2          *peer.AWG2Settings // non-nil = replace all AWG2 params
}

// InterfaceExport is the response body for export-params (S2S interconnect flow).
type InterfaceExport struct {
	Name         string `json:"name"`
	PublicKey    string `json:"publicKey"`
	Endpoint     string `json:"endpoint"`
	Address      string `json:"address"`
	Protocol     string `json:"protocol"`
	AllowedIPs   string `json:"allowedIPs,omitempty"`   // "0.0.0.0/0" for DisableRoutes ifaces
	PresharedKey string `json:"presharedKey,omitempty"` // included only if PSK already exists
}

