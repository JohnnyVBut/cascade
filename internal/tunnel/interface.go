// AddPeer creates a new peer in SQLite, updates the in-memory cache,
// regenerates the wg-quick config, and applies the peer to the kernel.
func (t *TunnelInterface) AddPeer(inp peer.PeerInput) (*peer.Peer, error) {
	// Auto-allocate the hub-side IP before inserting (needs in-memory peer map).
	if inp.AutoAllocateIP {
		ip, err := t.AutoAllocateIP()
		if err != nil {
			return nil, fmt.Errorf("auto-allocate IP: %w", err)
		}
		inp.AllowedIPs = ip
		inp.AutoAllocateIP = false
	}

	// Generate key triplet if requested (protocol-aware: wg vs awg).
	if inp.GenerateKeys {
		keys, err := peer.GenerateKeys(t.syncBin())
		if err != nil {
			return nil, fmt.Errorf("generate keys: %w", err)
		}
		inp.PrivateKey = keys.PrivateKey
		inp.PublicKey = keys.PublicKey
		inp.PresharedKey = keys.PresharedKey
		inp.GenerateKeys = false
	}

	// Interconnect peers always need a PSK for mutual authentication.
	// If none was provided (first importer in S2S workflow), generate one now.
	// The importer exports their params with this PSK so the remote side can
	// import it and the two ends end up with a matching PSK.
	if inp.PeerType == "interconnect" && inp.PresharedKey == "" {
		psk, err := peer.GeneratePSK(t.syncBin())
		if err != nil {
			return nil, fmt.Errorf("generate PSK for interconnect peer: %w", err)
		}
		inp.PresharedKey = psk
	}

	// Derive peer tunnel address from AllowedIPs + interface mask when not set.
	// address is shown in UI separately from AllowedIPs (e.g. AllowedIPs=0.0.0.0/0
	// but address=10.8.0.2/24 — the actual tunnel interface address of the peer).
	if inp.Address == "" && inp.AllowedIPs != "" && t.Address != "" {
		firstCIDR := strings.TrimSpace(strings.Split(inp.AllowedIPs, ",")[0])
		peerIP := strings.SplitN(firstCIDR, "/", 2)[0]
		if peerIP != "" && peerIP != "0.0.0.0" {
			ifaceMask := "24"
			if parts := strings.SplitN(t.Address, "/", 2); len(parts) == 2 {
				ifaceMask = parts[1]
			}
			inp.Address = peerIP + "/" + ifaceMask
		}
	}

	// Validate fields that will be passed to shell commands (command injection prevention).
	if inp.PublicKey != "" {
		if err := validate.WGKey(inp.PublicKey); err != nil {
			return nil, fmt.Errorf("invalid public key: %w", err)
		}
	}
	if inp.PresharedKey != "" {
		if err := validate.WGKey(inp.PresharedKey); err != nil {
			return nil, fmt.Errorf("invalid preshared key: %w", err)
		}
	}
	if inp.AllowedIPs != "" {
		if err := validate.CIDR(inp.AllowedIPs); err != nil {
			return nil, fmt.Errorf("invalid allowed IPs: %w", err)
		}
	}
	if inp.Endpoint != "" {
		if err := validate.Endpoint(inp.Endpoint); err != nil {
			return nil, fmt.Errorf("invalid endpoint: %w", err)
		}
	}
	// PrivateKey is written verbatim into the .conf file — validate to prevent
	// newline injection (e.g. "\nPostUp = <cmd>") that would execute on wg-quick up.
	// Empty is allowed: interconnect peers have no server-side private key (HIGH-3).
	if inp.PrivateKey != "" {
		if err := validate.WGKey(inp.PrivateKey); err != nil {
			return nil, fmt.Errorf("invalid private key: %w", err)
		}
	}
	if inp.ClientAllowedIPs != "" {
		if err := validate.CIDR(inp.ClientAllowedIPs); err != nil {
			return nil, fmt.Errorf("invalid client allowed IPs: %w", err)
		}
	}
	if inp.Address != "" {
		if err := validate.CIDR(inp.Address); err != nil {
			return nil, fmt.Errorf("invalid address: %w", err)
		}
	}

	p, err := peer.CreatePeer(t.ID, inp)
	if err != nil {
		return nil, err
	}

	t.peersMu.Lock()
	t.peers[p.ID] = p
	t.peersMu.Unlock()

	// Initialise traffic state for new peer (starts at zero, no prior traffic).
	t.trafficMu.Lock()
	if t.trafficState == nil {
		t.trafficState = make(map[string]*peerTrafficState)
	}
	t.trafficState[p.ID] = &peerTrafficState{}
	t.trafficMu.Unlock()

	if err := t.RegenerateConfig(); err != nil {
		return p, err
	}
	if t.Enabled {
		t.KernelSetPeer(p)
	}
	return p, nil
}

// UpdatePeer applies upd to the peer in SQLite and in-memory cache.
// Reloads kernel config only if WireGuard-relevant fields changed.
func (t *TunnelInterface) UpdatePeer(peerID string, upd peer.PeerUpdate) (*peer.Peer, error) {
	updated, err := peer.UpdatePeer(peerID, upd)
	if err != nil {
		return nil, err
	}

	t.peersMu.Lock()
	t.peers[peerID] = updated
	t.peersMu.Unlock()

	// Only reload the kernel when fields that appear in [Peer] config sections change.
	wgFields := upd.Enabled != nil || upd.AllowedIPs != nil ||
		upd.Endpoint != nil || upd.PersistentKeepalive != nil

	if wgFields {
		if err := t.RegenerateConfig(); err != nil {
			return updated, err
		}
		if t.Enabled {
			if updated.Enabled {
				t.KernelSetPeer(updated)
			} else {
				t.KernelRemovePeer(peerID)
			}
		}
	}
	return updated, nil
}

// RemovePeer deletes the peer from SQLite, removes it from the cache,
// regenerates config, and removes it from the running kernel.
func (t *TunnelInterface) RemovePeer(peerID string) error {
	if err := peer.DeletePeer(peerID); err != nil {
		return err
	}

	t.peersMu.Lock()
	delete(t.peers, peerID)
	t.peersMu.Unlock()

	t.trafficMu.Lock()
	delete(t.trafficState, peerID)
	t.trafficMu.Unlock()

	if err := t.RegenerateConfig(); err != nil {
		return err
	}
	if t.Enabled {
		t.KernelRemovePeer(peerID)
	}
	return nil
}

// ── Lifecycle ─────────────────────────────────────────────────────────────────

// quickBin returns the wg-quick equivalent for this interface's protocol (FIX-7).
func (t *TunnelInterface) quickBin() string {
	if t.Protocol == "amneziawg-2.0" {
		return "awg-quick"
	}
	return "wg-quick"
}

// syncBin returns the wg equivalent for this interface's protocol (FIX-7).
func (t *TunnelInterface) syncBin() string {
	if t.Protocol == "amneziawg-2.0" {
		return "awg"
	}
	return "wg"
}

// isUserspaceMode reports whether AmneziaWG is running in userspace mode.
// Userspace mode is active when WG_QUICK_USERSPACE_IMPLEMENTATION=amneziawg-go.
// In userspace mode: awg syncconf is stable (no kernel deadlock), so
// KernelRemovePeer can use Reload() instead of full Restart().
func isUserspaceMode() bool {
	return os.Getenv("WG_QUICK_USERSPACE_IMPLEMENTATION") == "amneziawg-go"
}

// Start regenerates the config file and brings up the interface (FIX-2).
//
// With --network host, the WireGuard interface lives in the host kernel and
// survives docker stop/start. Three failure modes are handled:
//
//   - "already exists": cycle down→up so PostUp (iptables MASQUERADE) re-executes.
//   - "Address in use": flush stale kernel routes left by a previous interface,
//     then retry.
func (t *TunnelInterface) Start() error {
	// Always regenerate before up — ensures PostUp/PostDown are current (FIX-2).
	if err := t.RegenerateConfig(); err != nil {
		return fmt.Errorf("start %s: regenerate config: %w", t.ID, err)
	}

	_, err := util.ExecDefault(fmt.Sprintf("%s up %s", t.quickBin(), t.ID))
	if err != nil {
		msg := err.Error()
		switch {
		case strings.Contains(msg, "already exists"):
			// Interface survived docker restart in --network host mode.
			// Cycling down→up re-executes PostUp (iptables MASQUERADE, etc.).
			log.Printf("tunnel: %s already exists — cycling down/up to apply PostUp", t.ID)
			if _, e := util.ExecDefault(fmt.Sprintf("%s down %s", t.quickBin(), t.ID)); e != nil {
				log.Printf("tunnel: %s down (cycle) error (continuing): %v", t.ID, e)
			}
			if _, e := util.ExecDefault(fmt.Sprintf("%s up %s", t.quickBin(), t.ID)); e != nil {
				return fmt.Errorf("start %s after cycle: %w", t.ID, e)
			}
		case strings.Contains(msg, "Address in use"):
			// Stale route from a previous wg interface left in the kernel.
			// Flush and retry once.
			log.Printf("tunnel: %s address in use — flushing stale routes and retrying", t.ID)
			subnet := cidrToSubnet(t.Address)
			util.ExecSilent(fmt.Sprintf("ip route del %s 2>/dev/null || true", subnet))
			util.ExecSilent(fmt.Sprintf("ip route del %s table local 2>/dev/null || true", subnet))
			if _, e := util.ExecDefault(fmt.Sprintf("%s up %s", t.quickBin(), t.ID)); e != nil {
				return fmt.Errorf("start %s after flush: %w", t.ID, e)
			}
		default:
			return fmt.Errorf("start %s: %w", t.ID, err)
		}
	}

	t.Enabled = true
	return t.save()
}

// Stop brings down the interface and ignores benign errors (FIX-3):
//   - interface already stopped ("not a WireGuard/AmneziaWG interface")
//   - iptables rule not found at PostDown time ("Bad rule", "does a matching rule exist")
//
// Flushes traffic totals to DB before bringing the interface down so that the
// kernel counters (which reset on wg-quick down) are not lost.
func (t *TunnelInterface) Stop() error {
	t.FlushTrafficTotals()
	_, err := util.ExecDefault(fmt.Sprintf("%s down %s", t.quickBin(), t.ID))
	if err != nil {
		ignored := []string{
			"is not a WireGuard interface",
			"is not an AmneziaWG interface",
			"iptables: Bad rule",
			"does a matching rule exist",
		}
		msg := err.Error()
		benign := false
		for _, s := range ignored {
			if strings.Contains(msg, s) {
				benign = true
				break
			}
		}
		if !benign {
			return fmt.Errorf("stop %s: %w", t.ID, err)
		}
	}

	t.Enabled = false
	return t.save()
}

// Restart stops and starts the interface.
func (t *TunnelInterface) Restart() error {
	if err := t.Stop(); err != nil {
		return err
	}
	return t.Start()
}

// Reload enqueues a hot-reload (awg/wg syncconf) in a background goroutine.
// Concurrent Reload/Restart calls are serialized by reloadMu so that the kernel
// never receives two concurrent syncconf calls on the same device (deadlock risk).
func (t *TunnelInterface) Reload() {
	go func() {
		t.reloadMu.Lock()
		defer t.reloadMu.Unlock()
		if err := t.doReload(); err != nil {
			log.Printf("tunnel: reload %s failed: %v", t.ID, err)
		}
	}()
}

// syncconfTimeout is the deadline for `awg/wg syncconf`.
// Mirrors amneziawg-installer v5.7.5 mitigation for upstream kernel deadlock #146:
// if the AWG kernel module hangs during syncconf, kill it after 10s and fall back
// to a full interface restart instead of hanging indefinitely.
const syncconfTimeout = 10 * time.Second

// doReload executes `<syncBin> syncconf <id> <(<quickBin> strip <id>)`.
// Uses bash process substitution — works because util.Exec runs via `bash -c`.
// Must be called with reloadMu held.
//
// On timeout or error (AWG kernel deadlock #146): falls back to full Restart().
// Restart() itself holds no locks, so calling it here (while reloadMu is held)
// is safe — any subsequent Reload() goroutine will simply queue behind reloadMu.
func (t *TunnelInterface) doReload() error {
	cmd := fmt.Sprintf("%s syncconf %s <(%s strip %s)",
		t.syncBin(), t.ID, t.quickBin(), t.ID)
	if _, err := util.Exec(cmd, syncconfTimeout, true); err != nil {
		// syncconf timed out or failed — likely AWG kernel module deadlock (#146).
		// Fall back to full down→up restart to recover a clean kernel state.
		log.Printf("tunnel: %s syncconf failed (%v) — falling back to full restart", t.ID, err)
		if restartErr := t.Restart(); restartErr != nil {
			return fmt.Errorf("syncconf failed and fallback restart also failed: %w", restartErr)
		}
		log.Printf("tunnel: %s recovered via full restart after syncconf failure", t.ID)
		return nil
	}
	log.Printf("tunnel: %s hot-reloaded (syncconf)", t.ID)
	return nil
}

// ── Kernel sync ───────────────────────────────────────────────────────────────

// KernelSetPeer applies a peer addition/update to the running kernel (FIX-9).
//
//   - AWG 2.0: calls Reload() (awg syncconf via quickBin strip).
//     `awg set peer` is unstable in the AWG kernel module (10-15+ seconds,
//     leaves kernel in bad state). syncconf is atomic and reliable.
//   - WireGuard 1.0: runs `wg set peer <key> …` which is fast and reliable.
//
// Fire-and-forget: returns immediately; work runs in background goroutine.
func (t *TunnelInterface) KernelSetPeer(p *peer.Peer) {
	if !t.Enabled {
		return
	}
	if t.Protocol == "amneziawg-2.0" {
		t.Reload() // Reload already serializes via reloadMu
		return
	}

	// WireGuard 1.0: wg set peer is reliable, run under reloadMu for safety.
	go func() {
		t.reloadMu.Lock()
		defer t.reloadMu.Unlock()

		// Write PSK to a temp file (wg set peer preshared-key takes a file path).
		var pskFile string
		if p.PresharedKey != "" {
			pskFile = fmt.Sprintf("/tmp/%s-psk-%s", t.ID, p.ID)
			if err := os.WriteFile(pskFile, []byte(p.PresharedKey+"\n"), 0600); err != nil {
				log.Printf("tunnel: KernelSetPeer %s: write psk file: %v", p.ID, err)
				return
			}
			defer os.Remove(pskFile)
		}

		cmd := fmt.Sprintf("%s set %s peer %s", t.syncBin(), t.ID, p.PublicKey)
		if pskFile != "" {
			cmd += " preshared-key " + pskFile
		}
		if p.AllowedIPs != "" {
			cmd += " allowed-ips " + p.AllowedIPs
		}
		if p.Endpoint != "" {
			cmd += " endpoint " + p.Endpoint
		}
		if p.PersistentKeepalive > 0 {
			cmd += " persistent-keepalive " + strconv.Itoa(p.PersistentKeepalive)
		}
		if _, err := util.ExecDefault(cmd); err != nil {
			log.Printf("tunnel: KernelSetPeer %s failed: %v", p.ID, err)
		}
	}()
}

// KernelRemovePeer removes a peer from the running kernel (FIX-8).
//
// Kernel mode: uses full Restart() (down + up).
//   Both `awg set peer remove` and `awg syncconf` deadlock in the AWG kernel module
//   after ~3–5 add/remove cycles. Restart is the safe alternative.
//   Downside: per-peer transfer stats are reset on each restart.
//
// Userspace mode: uses Reload() (awg syncconf).
//   The userspace daemon has no kernel deadlock — syncconf is stable and atomic.
//   Transfer stats are preserved across reloads.
//
// Fire-and-forget: returns immediately.
func (t *TunnelInterface) KernelRemovePeer(peerID string) {
	if !t.Enabled {
		return
	}
	if t.Protocol == "amneziawg-2.0" && isUserspaceMode() {
		// Userspace: syncconf is stable — use Reload() to preserve transfer stats.
		t.Reload()
		return
	}
	go func() {
		t.reloadMu.Lock()
		defer t.reloadMu.Unlock()
		if err := t.Restart(); err != nil {
			log.Printf("tunnel: KernelRemovePeer %s: restart failed: %v", peerID, err)
		}
	}()
}

