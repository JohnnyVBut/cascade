// Package tunnel manages WireGuard/AmneziaWG tunnel interfaces.
//
// Each TunnelInterface wraps one kernel interface (wg10, wg11, …) and owns:
//   - SQLite persistence (interfaces table)
//   - wg-quick / awg-quick lifecycle (start / stop / reload)
//   - in-memory peer cache (populated from SQLite, updated on mutations)
//   - runtime status polling (`wg/awg show dump`)
//
// Critical fixes ported from Node.js (CLAUDE.md):
//
//	FIX-1  iptables-nft + -A FORWARD (not -I), both -i and -o directions
//	FIX-2  RegenerateConfig before Start(); down→up on "already exists"
//	FIX-3  Stop() ignores "not a WireGuard/AmneziaWG interface"
//	FIX-7  quickBin/syncBin chosen by protocol
//	FIX-8  KernelRemovePeer → Restart for AWG2 (mutex, fire-and-forget goroutine)
//	FIX-9  KernelSetPeer   → Reload for AWG2, wg set peer for WG1 (mutex)
//	FIX-10 GetStatus uses ExecSilentFast (5 s timeout, no log — runs every second)
package tunnel

